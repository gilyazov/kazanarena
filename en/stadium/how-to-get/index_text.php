<h3 id="car">By car <img src="/bitrix/templates/ka/images/car@2x.png" width="22" height="16"  /></h3>
 	 
<h4>By the Yamashev Avenue</h4>
 
<p>Go by the avenue towards the road interchange at Yamashev-Chistopolskaya streets. At the interchange go under the bridge, then turn right and you'll be on the Chistopolskaya street. Go for about 300 meters and then turn left, to the Kazan Arena's parking lot.</p>
 
<h4>By Chistopolskaya Street</h4>
 
<p>The most convenient way to get to the stadium from the city's center is by Chistopolskaya street. If you are driving from the Millenium bridge take a turn to the right from the Amirkhana Avenue to the Chistopolskaya street. If you are driving from the Leninskaya dam (from the direction of Kazan Kremlin) take a turn to the right from the Dekabristov street to Chistopolskaya street. Going by Chistopolskaya street you'll get to the stadium, which will be on your right.</p>
<p>You can leave your car at the parking lot by the Arena.</p>
 
